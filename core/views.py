def create_ref_code():
    return ''.join(random.choices(string.ascii_lowercase + string.digits, k=20))


class PaymentView(View):
    def get(self, *args, **kwargs):
        try:
            order = Order.objects.get(user=self.request.user, ordered=False)
            if order.get_total() <= 0:
                messages.warning(self.request, "Your order total is zero, no payment needed")
                return redirect("core:order-summary")
                
            if not order.billing_address:
                messages.warning(self.request, "Please add a billing address first")
                return redirect("core:checkout")
                
            context = {
                'order': order,
                'DISPLAY_COUPON_FORM': False,
                'STRIPE_PUBLIC_KEY': settings.STRIPE_PUBLIC_KEY
            }
            return render(self.request, "payment.html", context)
            
        except ObjectDoesNotExist:
            messages.error(self.request, "You do not have an active order")
            return redirect("core:order-summary")

    def post(self, *args, **kwargs):
        order = Order.objects.get(user=self.request.user, ordered=False)
        token = self.request.POST.get('stripeToken')
        amount = int(order.get_total() * 100)  # cents
        
        if amount <= 0:
            messages.error(self.request, "Order total must be greater than zero")
            return redirect("core:order-summary")

        try:
            # Create the charge
            charge = stripe.Charge.create(
                amount=amount,
                currency="usd",
                source=token,
                description=f"Charge for order {order.id}"
            )

            # Create the payment
            payment = Payment()
            payment.stripe_charge_id = charge['id']
            payment.user = self.request.user
            payment.amount = order.get_total()
            payment.save()

            # Assign the payment to the order
            order_items = order.items.all()
            order_items.update(ordered=True)
            for item in order_items:
                item.save()

            order.ordered = True
            order.payment = payment
            order.ref_code = create_ref_code()
            order.save()

            messages.success(self.request, "Your order was successful!")
            return redirect("core:home")

        except stripe.error.CardError as e:
            body = e.json_body
            err = body.get('error', {})
            messages.error(self.request, f"Card error: {err.get('message')}")
            return redirect("core:payment")

        except stripe.error.StripeError as e:
            # Server error
            messages.error(self.request, "Stripe error occurred. Please try again.")
            return redirect("core:payment")

        except Exception as e:
            # Something else happened
            messages.error(self.request, "A serious error occurred. We have been notified.")
            # Send email to admin here
            return redirect("core:payment")


